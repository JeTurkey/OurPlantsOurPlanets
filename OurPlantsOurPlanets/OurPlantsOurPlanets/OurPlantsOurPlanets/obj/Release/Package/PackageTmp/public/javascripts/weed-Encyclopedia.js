$("#abutton").click(function () {
    $(".alphabet-content:visible").hide();
    $("#a").show();
    $(".alphabet").each(function () {
        if ($(this).hasClass("active")) {
            $(this).removeClass("active");
        }
    });
    $(this).parent().addClass('active');

});

